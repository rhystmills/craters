# Finds maximum values of x y and z for an list of dicts
def find_max_values(data):
    max_values = {
        'x':0,
        'y':0,
        'r':0
    }
    for row in data:
        if row['x'] > max_values['x']:
            max_values['x'] = row['x']
        if row['y'] > max_values['y']:
            max_values['y'] = row['y']
        if row['r'] > max_values['r']:
            max_values['r'] = row['r']
    return max_values

